def parse_msp(msp_text):
    """Parse MSP-format spectral library into list of dicts."""
    entries = []
    current = None
    for line in msp_text.strip().split('\n'):
        line = line.strip()
        if not line:
            if current and 'peaks' in current:
                entries.append(current)
            current = None
            continue
        if line.startswith('NAME:'):
            if current and 'peaks' in current:
                entries.append(current)
            current = {'name': line.split(':', 1)[1].strip().lower()}
        elif line.startswith('CASNO:') and current:
            current['cas'] = line.split(':', 1)[1].strip()
        elif line.startswith('FORMULA:') and current:
            current['formula'] = line.split(':', 1)[1].strip()
        elif line.startswith('RT_EXP:') and current:
            try:
                current['rt_exp'] = float(line.split(':', 1)[1].strip())
            except ValueError:
                pass
        elif line.startswith('NUM PEAKS:') and current:
            current['num_peaks'] = int(line.split(':', 1)[1].strip())
        elif ';' in line and current:
            peaks = []
            for pair in line.split(';'):
                pair = pair.strip()
                if not pair:
                    continue
                parts = pair.split()
                if len(parts) >= 2:
                    try:
                        mz = int(parts[0])
                        intensity = int(parts[1])
                        peaks.append((mz, intensity))
                    except ValueError:
                        continue
            if peaks:
                current['peaks'] = peaks
    if current and 'peaks' in current:
        entries.append(current)
    return entries

